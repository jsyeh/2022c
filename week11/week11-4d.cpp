///(SOIT106_BASE_004) 基礎題：計程車資計算 :
///輸入里程公尺數，輸出應付的車資。計程車資計算方式為：起跳100 元(2000公尺)，續跳5元(每500公尺)。
#include <stdio.h>
int main()
{
	int n;
	scanf("%d", &n);

	int ans;

	if(n<=2000) ans = 100;
	else {
		if(n%500==0) ans = 100 + (n-2000)/500*5;
		else ans = 100 + (n-2000)/500*5 + 5;
		//不整除的時候, 會多跳表一次5元,心很痛
	}
	printf("%d\n", ans);

}
