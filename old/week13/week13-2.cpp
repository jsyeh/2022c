///1 1 2 3 5 8 13 (21) (33)
///口訣: 前2項相加,得到新的項

#include <stdio.h>
int a[50];

int main()
{
    a[0] = 0;
    a[1] = 1;
    ///數學歸納法, a[0] a[1] 要先有,才能自動繼續
    for(int i=2; i<45; i++){ ///因為太大會爆炸!!
        a[i] = a[i-1] + a[i-2];
    }

    for(int i=0; i<45; i++){
        printf("%d ", a[i] );
    }
}
